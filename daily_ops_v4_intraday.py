def v4_inference(workspace: dict, df: pd.DataFrame) -> dict:
    print("\n" + "=" * 60)
    print("ğŸ¯ Step 3: V4 ç­–ç•¥æ¨è«– (ç›¤ä¸­é æ¸¬)")
    print("=" * 60)
    
    from stable_baselines3 import PPO
    latest = df.iloc[-1]
    signal_buy_filter = bool(latest.get('Signal_Buy_Filter', False))
    print(f"  [æ¿¾ç¶²] Signal_Buy_Filter = {signal_buy_filter}")
    
    features = np.array([latest.get(col, 0.0) for col in core_system.FEATURE_COLS], dtype=np.float32).reshape(1, -1)
    features = np.nan_to_num(features, nan=0.0, posinf=1.0, neginf=-1.0)
    
    # è¼‰å…¥ V4 æ¨¡å‹
    buy_path = os.path.join(STRATEGY_V4_PATH, 'ppo_buy_twii_final.zip')
    sell_path = os.path.join(STRATEGY_V4_PATH, 'ppo_sell_twii_final.zip')
    
    if not os.path.exists(buy_path):
        return {'error': 'V4 Model not found', 'filter_status': signal_buy_filter}

    try:
        buy_agent = PPO.load(buy_path)
        sell_agent = PPO.load(sell_path)
        
        b_act, _ = buy_agent.predict(features, deterministic=True)
        b_obs = buy_agent.policy.obs_to_tensor(features)[0]
        b_prob = buy_agent.policy.get_distribution(b_obs).distribution.probs.detach().cpu().numpy()[0]
        
        ai_action = 'BUY' if b_act[0] == 1 else 'WAIT'
        buy_prob = float(b_prob[1]) if b_act[0] == 1 else float(b_prob[0])
        
        buy_signal = ai_action if signal_buy_filter else f"FILTERED (AI: {ai_action})"
        print(f"  [V4] Buy: {buy_signal} ({buy_prob:.1%})")
        
        sell_scenarios = {}
        for scenario_name, return_value in {'cost': 1.00, 'profit': 1.10, 'loss': 0.95}.items():
            s_feat = np.concatenate([features[0], [return_value]]).reshape(1, -1)
            s_act, _ = sell_agent.predict(s_feat, deterministic=True)
            sell_scenarios[scenario_name] = 'SELL' if s_act[0] == 1 else 'HOLD'
        
        return {
            'filter_status': signal_buy_filter,
            'buy_signal': buy_signal,
            'buy_prob': buy_prob,
            'ai_action': ai_action,
            'sell_scenarios': sell_scenarios
        }
    except Exception as e:
        print(f"  [Error] V4 Inference: {e}")
        return {'error': str(e), 'filter_status': signal_buy_filter}


